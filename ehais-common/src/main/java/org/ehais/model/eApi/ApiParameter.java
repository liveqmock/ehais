package org.ehais.model.eApi;

public class ApiParameter {
	
	private String name;
	private String[] mustParameter;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String[] getMustParameter() {
		return mustParameter;
	}
	public void setMustParameter(String[] mustParameter) {
		this.mustParameter = mustParameter;
	}

	
}
